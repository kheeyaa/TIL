  function solution(nums, k) {
    const n = nums.length;
    let sorted = [];
    const pair = new Set();

    // k=0인 경우
    if (k === 0) {
      const map = new Map();
      for (const num of nums) {
        map.set(num, map.get(num) + 1 || 1);
      }
      for (const [num, cnt] of [...map]) {
        if (cnt > 1) sorted.push(num, num);
        else sorted.push(num);
      }
    } else {
      sorted = [...new Set(nums)].sort((a, b) => a - b);
    }

    let lt = 0;
    let rt = 1;
    while (rt < n) {
      if (Math.abs(sorted[lt] - sorted[rt]) <= k) {
        if (Math.abs(sorted[lt] - sorted[rt]) === k) {
          pair.add(`${sorted[lt]},${sorted[rt]}`);
        }
        rt++;
      } else {
        lt += 1;
        rt = lt + 1;
      }
    }

    return pair.size;
  }
